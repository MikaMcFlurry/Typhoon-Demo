const switchButton = document.getElementById('langSwitch');
const year = document.getElementById('year');
year.textContent = new Date().getFullYear();

function setLanguage(lang) {
  const selected = translations[lang] ? lang : 'de';
  localStorage.setItem('typhoon-language', selected);
  document.documentElement.lang = selected;

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[selected][key]) {
      element.textContent = translations[selected][key];
    }
  });

  const title = selected === 'de'
    ? 'Typhoon | Bluesrock, Funk, Soul & Southern Rock aus Hechingen'
    : 'Typhoon | Bluesrock, Funk, Soul & Southern Rock from Hechingen';

  const description = selected === 'de'
    ? 'Typhoon aus Hechingen verbindet Bluesrock, Funk, Soul, Jazz und Southern Rock mit türkischsprachigen Texten, markanten Riffs, Bläsersatz und viel Groove.'
    : 'Typhoon from Hechingen blends bluesrock, funk, soul, jazz and southern rock with Turkish lyrics, strong riffs, a horn section and deep groove.';

  document.title = title;
  document.querySelector('meta[name="description"]').setAttribute('content', description);
  document.querySelector('meta[property="og:title"]').setAttribute('content', title);
  document.querySelector('meta[property="og:description"]').setAttribute('content', description);
}

switchButton.addEventListener('click', () => {
  const nextLang = document.documentElement.lang === 'de' ? 'en' : 'de';
  setLanguage(nextLang);
});

const storedLanguage = localStorage.getItem('typhoon-language') || 'de';
setLanguage(storedLanguage);

const players = document.querySelectorAll('audio');
players.forEach((player) => {
  player.addEventListener('play', () => {
    players.forEach((otherPlayer) => {
      if (otherPlayer !== player) {
        otherPlayer.pause();
      }
    });
  });
});
