let currentLang = localStorage.getItem("typhoon-lang") || "de";

const setLanguage = (lang) => {
  currentLang = lang;
  document.documentElement.lang = lang;
  localStorage.setItem("typhoon-lang", lang);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (content[lang][key]) {
      element.innerHTML = content[lang][key];
    }
  });

  document.getElementById("langToggle").textContent = lang === "de" ? "EN" : "DE";
  renderSongs();
  renderEvents();
  renderNews();
  updateSeo(lang);
};

const renderSongs = () => {
  const songsGrid = document.getElementById("songsGrid");
  songsGrid.innerHTML = "";

  content[currentLang].songs.forEach((song, index) => {
    const card = document.createElement("article");
    card.className = "song-card";
    card.innerHTML = `
      <span class="song-index">Track ${String(index + 1).padStart(2, "0")}</span>
      <h3>${song}</h3>
      <p class="meta">Typhoon</p>
    `;
    songsGrid.appendChild(card);
  });
};

const renderEvents = () => {
  const eventsGrid = document.getElementById("eventsGrid");
  eventsGrid.innerHTML = "";
  const events = content[currentLang].events;

  if (!events.length) {
    eventsGrid.innerHTML = `<div class="item-card empty-state">${content[currentLang].eventEmpty}</div>`;
    return;
  }

  events.forEach((event) => {
    const card = document.createElement("article");
    card.className = "item-card";
    card.innerHTML = `
      <span class="tag">${content[currentLang].eventTag}</span>
      <h3>${event.title}</h3>
      <p class="meta"><strong>${event.date}</strong> · ${event.location}</p>
      <p>${event.text}</p>
    `;
    eventsGrid.appendChild(card);
  });
};

const renderNews = () => {
  const newsGrid = document.getElementById("newsGrid");
  newsGrid.innerHTML = "";
  const news = content[currentLang].news;

  if (!news.length) {
    newsGrid.innerHTML = `<div class="item-card empty-state">${content[currentLang].newsEmpty}</div>`;
    return;
  }

  news.forEach((entry) => {
    const card = document.createElement("article");
    card.className = "item-card";
    card.innerHTML = `
      <span class="tag">${content[currentLang].newsTag}</span>
      <h3>${entry.title}</h3>
      <p class="meta">${entry.date}</p>
      <p>${entry.text}</p>
    `;
    newsGrid.appendChild(card);
  });
};

const updateSeo = (lang) => {
  const seo = {
    de: {
      title: "Typhoon | Bluesrock mit Funk- und Soul-Touch",
      description: "Typhoon ist eine 8-köpfige Band aus Hechingen mit Bluesrock, Funk- und Soul-Touch – viel Groove, kräftige Riffs und Southern-Vibe."
    },
    en: {
      title: "Typhoon | Blues rock with a funk and soul touch",
      description: "Typhoon is an 8-piece band from Hechingen blending blues rock, funk and soul — groove, strong riffs and a Southern vibe."
    }
  };

  document.title = seo[lang].title;
  document.querySelector('meta[name="description"]').setAttribute("content", seo[lang].description);
  document.querySelector('meta[property="og:title"]').setAttribute("content", seo[lang].title);
  document.querySelector('meta[property="og:description"]').setAttribute("content", seo[lang].description);
  document.querySelector('meta[name="twitter:title"]').setAttribute("content", seo[lang].title);
  document.querySelector('meta[name="twitter:description"]').setAttribute("content", seo[lang].description);
};

document.getElementById("langToggle").addEventListener("click", () => {
  setLanguage(currentLang === "de" ? "en" : "de");
});

document.getElementById("year").textContent = new Date().getFullYear();
setLanguage(currentLang);
